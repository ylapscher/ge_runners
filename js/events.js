function makeInlineBlock(div) {
    var x = document.getElementById(div);
    x.style.display = "inline-block";
}

function makeBlock(div) {
    var x = document.getElementById(div);
    x.style.display = "block";
}

function hide(div) {
    var x = document.getElementById(div);
    x.style.display = "none";
}

